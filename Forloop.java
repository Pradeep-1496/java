import java.util.*;
public class Forloop {
    public static void main(String[] args) {
        int n;
        Scanner sc = new Scanner(System.in);
        System.out.print("Enter the number: ");
        n = sc.nextInt();
      for (int i = 0; i <= n; ++i) {
        System.out.println(i);
      }  
    }
  }
  